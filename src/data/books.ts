export const booksByYear = (): Record<number, Book[]> => {
  const grouped: Record<number, Book[]> = {}
  for (const book of books) {
    if (!grouped[book.year]) grouped[book.year] = []
    grouped[book.year].push(book)
  }
  return grouped
}

export const years = (): number[] =>
  [...new Set(books.map(b => b.year))].sort((a, b) => b - a)
